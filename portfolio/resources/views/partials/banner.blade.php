<script>
    $(document).ready(function() {
        let messages = ["FullStack Developer !", "Web Desiner !", "Freelancer !"];
        let messageIndex = 0;
        let charIndex = 0;
        let displayText = "";
        let isDeleting = false;

        function updateText() {
            let currentMessage = messages[messageIndex];

            if (!isDeleting) {
                // Typing effect: Add one character at a time
                if (charIndex < currentMessage.length) {
                    displayText += currentMessage[charIndex];
                    charIndex++;
                    setTimeout(updateText, 100); // 1s delay per character
                } else {
                    // After typing, wait 2 seconds before deleting
                    setTimeout(() => {
                        isDeleting = true;
                        updateText();
                    }, 1000);
                }
            } else {
                // Erasing effect: Remove one character at a time
                if (charIndex > 0) {
                    displayText = displayText.slice(0, -1);
                    charIndex--;
                    setTimeout(updateText, 100); // 0.5s delay per character removal
                } else {
                    // After deleting, move to the next message
                    isDeleting = false;
                    messageIndex = (messageIndex + 1) % messages.length; // Loop through messages
                    setTimeout(updateText, 1000); // Wait 1s before typing the next message
                }
            }

            // Always append `_` at the end as a cursor effect
            $("#content").text(displayText + " _");
        }

        // Start the animation
        updateText();
    });
</script>